package casetoolsAssignment;

public class Dog {

	private String name, breed;
	
	public Dog(String n, String b) {
		name = n;
		breed = b;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBreed() {
		return breed;
	}

	public void setBreed(String breed) {
		this.breed = breed;
	}
	
	
	
}
